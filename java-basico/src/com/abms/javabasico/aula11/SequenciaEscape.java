package com.abms.javabasico.aula11;

public class SequenciaEscape {
    public static void main(String[] args) {
        //"Hello World!"
        System.out.println("Hello World!");
        System.out.println("\"Hello World!\"");
        System.out.print("\"Hello World!\"\n\r");
        System.out.println("1\\4");
    }
}
